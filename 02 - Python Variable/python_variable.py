# Variables

a = str(3)    # a bakal jadi '3' 
b = int(3)    # b bakal jadi 3 
c = float(3)  # c bakal jadi 3.0 
print(a)
print(b)
print(c)
print("-" * 30)

# Contoh singkat
x = 5
name = "Iman"
berat = 72.5
print(x)
print(name)
print(berat)
print("-" * 30)

# cari tipe data
angka = 5
nama = "Pudol"
tinggi_badan = 175.9
print(type(angka))  # (type) Untuk asih tau tipe datanya jenis apa
print(type(nama))
print(type(tinggi_badan))
print("-" * 30)

# Case-Sensitive
# Nama variabel di python sangat sensitif 

s = 4
