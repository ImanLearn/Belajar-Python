# Setting the Data Type
    # Dalam Python, tipe data ditentukan saat Anda memberikan nilai ke sebuah variabel:

# 1. Tipe Data str
x = "Hello World"
print(x)
print(type(x))
print("------------------------------")

# 2. Tipe Data int
x = 20
print(x)
print(type(x))
print("------------------------------")

# 3. Tipe Data float
x = 20.5
print(x)
print(type(x))
print("------------------------------")

# 4. Tipe Data complex
x = 3 + 4j
print(x)
print(type(x))
print("------------------------------")

# 5. Tipe Data list ( bisa di ubah [] )
x = ["apple", "banana", "cherry"]
print(x)
print(type(x))
print("------------------------------")

# 6. Tipe Data tuple (Mutlak () )
x = ("apple", "banana", "cherry")
print(x)
print(type(x))
print("------------------------------")

# 7. Tipe Data range
x = range(6)
print(x)
print(type(x))
print("------------------------------")

# 8. Tipe Data dictianory
x = {"name" : "John", "age" : 36}
print(x)
print(type(x))
print("------------------------------")

# 9. Tipe Data set
x = {"apple", "banana", "cherry"}
print(x)
print(type(x))
print("------------------------------")

# 10. Tipe Data frozenset
x = frozenset({"apple", "banana", "cherry"})
print(x)
print(type(x))
print("------------------------------")

# 11. Tipe Data bool
x = True
print(x)
print(type(x))
print("------------------------------")

# 12. Tipe Data bytes
x = b"Hello"
print(x)
print(type(x))
print("------------------------------")

# 13. Tipe Data bytearray
x = bytearray(5)
print(x)
print(type(x))
print("------------------------------")

# 14. Tipe Data memoryview
x = memoryview(bytes(5))
print(x)
print(type(x))

# 15. Tipe Data NoneType
x = None
print(x)
print(type(x))