# PYTHON ARITHMETIC OPERATORS
# ==========================================

x = 10
y = 3

# 1. ADDITION (+) 
# Fungsi: Penjumlahan biasa.
tambah = x + y
print("1. Penjumlahan (10 + 3) =", tambah) 
# Output: 13


# 2. SUBTRACTION (-)
# Fungsi: Pengurangan biasa.
kurang = x - y
print("2. Pengurangan (10 - 3) =", kurang) 
# Output: 7


# 3. MULTIPLICATION (*)
# Fungsi: Perkalian.
kali = x * y
print("3. Perkalian (10 * 3) =", kali) 
# Output: 30


# 4. DIVISION (/) 
# Fungsi: Pembagian biasa (Hasilnya SELALU berupa Float/Desimal).
bagi = x / y
print("4. Pembagian (10 / 3) =", bagi) 
# Output: 3.3333333333333335


# 5. MODULUS (%) 
# Fungsi: Mencari SISA HASIL BAGI. 
# (Penting banget nanti buat nyari angka ganjil/genap atau algoritma kriptografi!)
sisa_bagi = x % y
print("5. Modulus/Sisa Bagi (10 % 3) =", sisa_bagi) 
# Output: 1 (Karena 10 dibagi 3 itu dapet 3, sisa 1)


# 6. EXPONENTIATION (**) 
# Fungsi: Pangkatan (x pangkat y).
pangkat = x ** y
print("6. Perpangkatan (10 pangkat 3) =", pangkat) 
# Output: 1000


# 7. FLOOR DIVISION (//)  
# Fungsi: Pembagian bulat (Membulatkan hasil pembagian ke bawah, ngilangin komanya).
pembulatan_bawah = x // y
print("7. Floor Division (10 // 3) =", pembulatan_bawah) 
# Output: 3 (Komanya dibuang total)